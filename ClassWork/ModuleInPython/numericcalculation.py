# Numeric calculation
#--------------------------------------------------
# Function to add two numbers
def addNumbers(x,y):
    return (x+y)
#-----------------------------------------------
# Function to subtract two numbers 
def calculatedifference(x,y):
    return (x-y)
#------------------------------------------------
# Function to product of two numbers
def calculateproduct(x,y):
    return (x*y)
#-----------------------------------------------
# Function to calculate division of two numbers 
def dividenumbers(x,y):
    return (x/y)
#-----------------------------------------------
# Function to calculate modulus of two numbers 
def calculatemodule(x,y):
    return (x%y)
